# Creating a Set 
set1 = set() 
print("Intial blank Set: ") 
print(set1) 
  
# Creating a Set with  
# the use of a String 
set1 = set("COMP1819ADS") 
print("\nSet with the use of String: ") 
print(set1) 
  
# Creating a Set with 
# the use of a List 
set1 = set(["COMP", "1819", "ADS"]) 
print("\nSet with the use of List: ") 
print(set1) 
